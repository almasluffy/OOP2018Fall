
import java.util.Scanner;

public class Second {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int x = input.nextInt();
        for(int i =1;i<=10;i++)
        {
            System.out.println(x + " x " + i +" = " + (x*i));
        }

    }
}
